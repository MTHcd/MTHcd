-- Table City

CREATE TABLE CITY (ID INT AUTO_INCREMENT PRIMARY KEY, NAME VARCHAR(20) NOT NULL, COUNTRYCODE VARCHAR(5) NOT NULL,
DISTRICT VARCHAR(20) NOT NULL, POPULATION INT NOT NULL);

