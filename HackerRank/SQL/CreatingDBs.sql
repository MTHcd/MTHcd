-- Table Station

CREATE TABLE STATION (ID INT AUTO_INCREMENT PRIMARY KEY, CITY VARCHAR(20) NOT NULL, STATE VARCHAR(20) NOT NULL,
  LAT_N INT NOT NULL, LONG_W INT NOT NULL);

-- Solutions to HackerRank Question

SELECT DISTINCT CITY FROM STATION
WHERE ID % 2 = 0;

SELECT COUNT(*) - COUNT(DISTINCT(CITY)) FROM STATION;

(
SELECT CITY, LENGTH(CITY) FROM STATION
WHERE LENGTH(CITY) = (SELECT MIN(LENGTH(CITY)) FROM STATION)
ORDER BY CITY
LIMIT 1
)
UNION ALL
(
SELECT CITY, LENGTH(CITY) FROM STATION
WHERE LENGTH(CITY) = (SELECT MAX(LENGTH(CITY)) FROM STATION)
ORDER BY CITY
LIMIT 1
);

